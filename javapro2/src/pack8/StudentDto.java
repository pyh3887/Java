package pack8;

public class StudentDto {
	
	private String hakbun, irum;	//타입이 다른 여러개의 변수를 행단위로 움직임 레코드형
	private int jumsu;
	
	

	public String getHakbun() {
		return hakbun;
	}

	public void setHakbun(String hakbun) {
		this.hakbun = hakbun;
	}

	public String getIrum() {
		return irum;
	}

	public void setIrum(String irum) {
		this.irum = irum;
	}

	public int getJumsu() {
		return jumsu;
	}

	public void setJumsu(int jumsu) {
		this.jumsu = jumsu;
	}
	
	
	
	
	
	
	
	

}
